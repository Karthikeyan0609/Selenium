package week1.day1;

public class IsPrime {
public static void main(String[] args) {
	System.out.println("Prime Logic Logic start");
	int n=13;
	for (int i = 2; i < n-1; i++) {
		if(i%n==13) {
			System.out.println("It is Prime Number");
		}
		System.out.println("Not a prime");
		break;
	}
}
}
